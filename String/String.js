// String

console.log(typeof 'Hello, World!')
console.log(typeof "Hello, World!")
console.log(typeof `Hello, World!`)  // Template Literals

// Sequências de Escape

console.log('Hello, World!\n')  // '\n' -> Quebra de linha
console.log('\tHello, World!')  // '\t' -> Tabulação

// Template Literals

let programming_language = 'JavaScript'

console.log(`I'm programming in ${programming_language}`)
console.log(`5 + 10 is ${5 + 10}`)

// Concatenação

console.log('Hello, ' + 'World!')
