// Criando variáveis ou constantes em JavaScript

// Podemos declarar variáveis com duas palavras reservadas: 'var' e 'let'

// É recomendável usar 'let' para declarar variáveis

// Declaramos uma constante com a palavra reservada 'const'

let number = 10  // Declarando e inicializando uma variável
let my_name = "Lucas"

console.log(number)
console.log(my_name)

const PI = 3.14  // Declarando e inicializando uma constante
const E = 0.57

console.log(PI)
console.log(E)

// Conclusão: variáveis permitem que os valores contidos nelas sejam alterado em qualquer momento do programa, enquanto as constantes não permitem este comportamento.
