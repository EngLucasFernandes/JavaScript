// Em JavaScript, por causa da tipagem fraca, não é possível declarar uma variável que armazene um determinado tipo de dado, por isso as variáveis podem armazenar um tipo de dado em um momento e depois armazenar outro tipo de dado.

let language = "JavaScript"
console.log(language)
console.log(typeof language)  // Tipo da variável 'language' no momento

language = 10
console.log(language)
console.log(typeof language)
